/* ============================================================================
   Target A: Terminal-like display
   ============================================================================ */

static void targetA(void) {
    if (!initFramebuffer())
        panic("No framebuffer");

    clearScreen(0xFF001030u);
    writeString(20, 20, "pragma os", 0xFF00FF00u, 0xFF001030u);
    writeString(20, 60, PROMPT, 0xFFFFFFFFu, 0xFF001030u);

    DiskFSInit();
    FSInit();
    initInterrupts();

    while (1) {
        __asm__ __volatile__("sti\nhlt");
    }
}

/* ============================================================================
   Target B: Logo display
   ============================================================================ */

extern uint8_t _binary_logo_bin_start;
extern uint8_t _binary_logo_bin_end;

#define LOGO_WIDTH  1280
#define LOGO_HEIGHT 800

static void blitBackground(void) {
    uint32_t *logoData = (uint32_t *)&_binary_logo_bin_start;
    int w = (int)fb->width  < LOGO_WIDTH  ? (int)fb->width  : LOGO_WIDTH;
    int h = (int)fb->height < LOGO_HEIGHT ? (int)fb->height : LOGO_HEIGHT;

    for (int y = 0; y < h; y++)
        for (int x = 0; x < w; x++)
            putPixel(x, y, logoData[y * LOGO_WIDTH + x]);

    /* Fill rows below the logo */
    for (int y = h; y < (int)fb->height; y++)
        for (int x = 0; x < (int)fb->width; x++)
            putPixel(x, y, BG);

    /* Fill columns to the right of the logo */
    for (int y = 0; y < h; y++)
        for (int x = w; x < (int)fb->width; x++)
            putPixel(x, y, BG);
}

static void targetB(void) {
    if (!initFramebuffer())
        panic("No framebuffer");

    blitBackground();

    while (1) {
        __asm__ __volatile__("sti\nhlt");
    }
}

