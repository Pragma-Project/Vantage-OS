/* ============================================================================
   Minimal In-Memory Filesystem
   ============================================================================ */

#define FS_MAX_DIRS    8
#define FS_MAX_ENTRIES 16
#define FS_NAME_LEN    32
#define FS_PATH_LEN    32

typedef enum {
    MemFSFile,
    MemFSDir
} MemFSObjectType;

typedef struct {
    char           name[FS_NAME_LEN];
    MemFSObjectType ftype;
    int            size;
} MemFSObject;

typedef struct {
    char        path[FS_PATH_LEN];
    MemFSObject entries[FS_MAX_ENTRIES];
    int         count;
    int         used;
} MemFSDirSlot;

static MemFSDirSlot FSDirectoryTable[FS_MAX_DIRS];

static void strToName(char dst[FS_NAME_LEN], const char *src) {
    int i = 0;
    while (src[i] != '\0' && i < FS_NAME_LEN - 1) {
        dst[i] = src[i]; i++;
    }
    dst[i] = '\0';
}

static void strToPath(char dst[FS_PATH_LEN], const char *src) {
    int i = 0;
    while (src[i] != '\0' && i < FS_PATH_LEN - 1) {
        dst[i] = src[i]; i++;
    }
    dst[i] = '\0';
}

static int nameEq(const char *a, const char *b) {
    int i = 0;
    while (b[i] != '\0') {
        if (a[i] != b[i]) return 0;
        i++;
    }
    return a[i] == '\0';
}

static MemFSDirSlot *FSList(const char *path) {
    for (int i = 0; i < FS_MAX_DIRS; i++) {
        if (FSDirectoryTable[i].used && nameEq(FSDirectoryTable[i].path, path))
            return &FSDirectoryTable[i];
    }
    return (void *)0;
}

static void FSInit(void) {
    for (int i = 0; i < FS_MAX_DIRS; i++) {
        FSDirectoryTable[i].used  = 0;
        FSDirectoryTable[i].count = 0;
    }
    strToPath(FSDirectoryTable[0].path, "/");
    FSDirectoryTable[0].used  = 1;
    FSDirectoryTable[0].count = 2;

    strToName(FSDirectoryTable[0].entries[0].name, "docs");
    FSDirectoryTable[0].entries[0].ftype = MemFSDir;
    FSDirectoryTable[0].entries[0].size  = 0;

    strToName(FSDirectoryTable[0].entries[1].name, "readme.txt");
    FSDirectoryTable[0].entries[1].ftype = MemFSFile;
    FSDirectoryTable[0].entries[1].size  = 1234;
}

/* ============================================================================
   Size formatting helpers
   ============================================================================ */

typedef enum { fsuB, fsuKB, fsuMB, fsuGB } FSSizeUnit;

static int intToStr(int v, char buf[32], int start) {
    if (v == 0) {
        buf[start] = '0';
        return start + 1;
    }
    char tmp[16];
    int  len = 0;
    while (v > 0 && len < 16) {
        tmp[len++] = '0' + (v % 10);
        v /= 10;
    }
    int pos = start;
    for (int i = len - 1; i >= 0; i--)
        buf[pos++] = tmp[i];
    return pos;
}

static int appendCStr(char buf[32], int pos, const char *s) {
    int i = 0;
    while (s[i] != '\0' && pos < 31) {
        buf[pos++] = s[i++];
    }
    buf[pos] = '\0';
    return pos;
}

static void formatSize(int size, char buf[32]) {
    static const int KB = 1000;
    static const int MB = 1000000;
    static const int GB = 1000000000;

    int        n;
    FSSizeUnit unit;

    if      (size >= GB) { n = size / GB; unit = fsuGB; }
    else if (size >= MB) { n = size / MB; unit = fsuMB; }
    else if (size >= KB) { n = size / KB; unit = fsuKB; }
    else                 { n = size;      unit = fsuB;  }

    if      (unit == fsuB  && n >= 1000) { n /= KB; unit = fsuKB; }
    else if (unit == fsuKB && n >= 1000) { n /= KB; unit = fsuMB; }
    else if (unit == fsuMB && n >= 1000) { n /= KB; unit = fsuGB; }

    int p = intToStr(n, buf, 0);

    const char *unitStr;
    switch (unit) {
        case fsuB:  unitStr = " B";  break;
        case fsuKB: unitStr = " KB"; break;
        case fsuMB: unitStr = " MB"; break;
        default:    unitStr = " GB"; break;
    }
    appendCStr(buf, p, unitStr);
}

/* ============================================================================
   Serial Port Debug Output
   ============================================================================ */

static void serialWrite(char c) {
    /* COM1 base port: 0x3F8 */
    __asm__ __volatile__(
        "movb %0, %%al\n"
        "movw $0x3F8, %%dx\n"
        "outb %%al, %%dx\n"
        : : "r"(c) : "al", "dx"
    );
}

static void debugPrint(const char *s) {
    for (int i = 0; s[i] != '\0'; i++)
        serialWrite(s[i]);
    serialWrite('\n');
}

static void debugHex(uint64_t val) {
    static const char hexChars[] = "0123456789ABCDEF";
    for (int i = 15; i >= 0; i--) {
        uint64_t nibble = (val >> (i * 4)) & 0xF;
        serialWrite(hexChars[nibble]);
    }
    serialWrite('\n');
}

/* ============================================================================
   Panic / Halt
   ============================================================================ */

static void __attribute__((noreturn)) panic(const char *msg) {
    (void)msg;
    while (1) {
        __asm__ __volatile__("cli\nhlt");
    }
}

/* ============================================================================
   Framebuffer Graphics Primitives
   ============================================================================ */

static LimineFramebuffer *fb = (void *)0;

static int initFramebuffer(void) {
    if (framebufferRequest.response == (void *)0) return 0;
    if (framebufferRequest.response->framebufferCount == 0) return 0;
    fb = *framebufferRequest.response->framebuffers;
    return 1;
}

static inline void putPixel(int x, int y, uint32_t color) {
    if (fb == (void *)0) return;
    if (x < 0 || y < 0) return;
    if (x >= (int)fb->width || y >= (int)fb->height) return;

    int       offset = y * (int)fb->pitch + x * 4;
    uint32_t *pixel  = (uint32_t *)((uintptr_t)fb->address + (uintptr_t)offset);
    *pixel = color;
}

static void clearScreen(uint32_t color) {
    if (fb == (void *)0) return;
    for (int y = 0; y < (int)fb->height; y++)
        for (int x = 0; x < (int)fb->width; x++)
            putPixel(x, y, color);
}

