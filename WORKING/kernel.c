static void putChar(int x, int y, char ch, uint32_t fg, uint32_t bg) {
    if ((unsigned char)ch < 32 || (unsigned char)ch > 126) return;
    int idx = ((unsigned char)ch - 32) * 16;
    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = font8x16[idx + row];
        for (int col = 0; col < FONT_WIDTH; col++) {
            int bit = (bits >> (7 - col)) & 1;
            putPixel(x + col, y + row, bit ? fg : bg);
        }
    }
}

static void writeString(int x, int y, const char *s, uint32_t fg, uint32_t bg) {
    int cx = x;
    for (int i = 0; s[i] != '\0'; i++) {
        putChar(cx, y, s[i], fg, bg);
        cx += FONT_WIDTH;
    }
}

/* ============================================================================
   DiskFS shell command
   ============================================================================ */

#define FS_SIZE_COL (20 + 20 * FONT_WIDTH)

static void cmdLs(const char *path) {
    (void)path;
    DiskFSListResult list = DiskFSListRoot();
    char sizeBuf[32];

    if (list.count == 0) {
        writeString(20, curY, "(disk not ready)", 0xFF888888u, BG);
        curY += FONT_HEIGHT;
        return;
    }
    for (int idx = 0; idx < list.count; idx++) {
        FSObject *ep = &list.entries[idx];
        writeString(20, curY, ep->Name, FG_INPUT, BG);
        formatSize((int)ep->Size, sizeBuf);
        uint32_t color = (ep->FSType == FS_FILE) ? 0xFF88FF88u : 0xFF88CCFFu;
        writeString(FS_SIZE_COL, curY, sizeBuf, color, BG);
        curY += FONT_HEIGHT;
    }
}

/* ============================================================================
   Keyboard Handler
   ============================================================================ */

void keyboardHandler(void) {
    uint8_t sc = inb(0x60);

    /* Ignore key releases (bit 7 set) */
    if (sc & 0x80) {
        picEOI();
        return;
    }

    char ch = scancodeMap[sc];
    switch (ch) {
    case '\0':
        break;
    case '\b':
        if (curX > INPUT_X0) {
            curX -= FONT_WIDTH;
            putChar(curX, curY, ' ', BG, BG);
            if (inputLen > 0)
                inputLen--;
        }
        break;
    case '\n':
        /* Null-terminate and dispatch */
        inputBuf[inputLen] = '\0';
        curY += FONT_HEIGHT;
        curX = 20;
        /* Command dispatch */
        if (inputLen == 2 && inputBuf[0] == 'l' && inputBuf[1] == 's')
            cmdLs("/");
        /* Reset buffer and draw new prompt */
        inputLen = 0;
        writeString(curX, curY, PROMPT, 0xFFFFFFFFu, BG);
        curX = INPUT_X0;
        break;
    default:
        if (inputLen < 127) {
            inputBuf[inputLen] = ch;
            inputLen++;
        }
        putChar(curX, curY, ch, FG_INPUT, BG);
        curX += FONT_WIDTH;
        break;
    }

    picEOI();
}

static void initInterrupts(void) {
    picRemap();
    /* INT 33 = IRQ1 (keyboard). 0x28 = Limine kernel CS. 0x8E = present interrupt gate */
    setIdtGate(33, (uint64_t)(uintptr_t)keyboard_isr_stub, 0x28, 0x8E);
    loadIdt();
}

