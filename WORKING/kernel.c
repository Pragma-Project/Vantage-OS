/* ============================================================================
   PIC (8259) Remapping
   ============================================================================ */

#define PIC1_CMD  0x20u
#define PIC1_DATA 0x21u
#define PIC2_CMD  0xA0u
#define PIC2_DATA 0xA1u
#define PIC_EOI   0x20u

static void picRemap(void) {
    /* ICW1: begin initialisation */
    outb(PIC1_CMD, 0x11); ioWait();
    outb(PIC2_CMD, 0x11); ioWait();
    /* ICW2: vector offsets — IRQ0-7 -> INT 32-39, IRQ8-15 -> INT 40-47 */
    outb(PIC1_DATA, 0x20); ioWait();
    outb(PIC2_DATA, 0x28); ioWait();
    /* ICW3: cascade wiring */
    outb(PIC1_DATA, 4); ioWait();
    outb(PIC2_DATA, 2); ioWait();
    /* ICW4: 8086 mode */
    outb(PIC1_DATA, 0x01); ioWait();
    outb(PIC2_DATA, 0x01); ioWait();
    /* Mask all interrupts except IRQ1 (keyboard) */
    outb(PIC1_DATA, 0xFD);
    outb(PIC2_DATA, 0xFF);
}

static inline void picEOI(void) {
    outb(PIC1_CMD, PIC_EOI);
}

