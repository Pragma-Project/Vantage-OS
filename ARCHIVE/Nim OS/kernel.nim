# Target A: Terminal-like display
# ============================================================================

proc targetA() =
  if not initFramebuffer():
    panic("No framebuffer")

  # Clear screen to dark blue
  clearScreen(0xFF001030'u32)

  # Title
  writeString(20, 20, "nimos. (VMware test)", 0xFF00FF00'u32, 0xFF001030'u32)

  # Prompt
  writeString(20, 60, PROMPT, 0xFFFFFFFF'u32, 0xFF001030'u32)

  # Initialise disk filesystem
  DiskFSInit()

  # Initialise in-memory filesystem
  FSInit()

  # Set up interrupts and enable keyboard
  initInterrupts()

  # Wait for keyboard interrupts
  while true:
    asm """
      sti
      hlt
    """

