var fb: ptr LimineFramebuffer = nil

proc initFramebuffer(): bool =
  if framebufferRequest.response == nil:
    return false
  if framebufferRequest.response.framebufferCount == 0:
    return false
  fb = framebufferRequest.response.framebuffers[]
  return true

proc putPixel(x, y: int, color: uint32) {.inline.} =
  if fb == nil: return
  if x < 0 or y < 0: return
  if x >= fb.width.int or y >= fb.height.int: return

  let offset = y * fb.pitch.int + x * 4
  let pixel = cast[ptr uint32](cast[uint](fb.address) + offset.uint)
  pixel[] = color

proc clearScreen(color: uint32) =
  if fb == nil: return
  for y in 0..<fb.height.int:
    for x in 0..<fb.width.int:
      putPixel(x, y, color)

