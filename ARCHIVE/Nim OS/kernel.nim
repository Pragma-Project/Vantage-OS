# Change this to switch between targets
const USE_TARGET_A = true  # Set to false for Target B

proc kmain() {.exportc.} =
  debugPrint("kmain started")

  # Test if bootloader info request worked
  if bootloaderInfoRequest.response == nil:
    debugPrint("ERROR: bootloader info request failed")
    while true:
      asm "cli\nhlt"
  debugPrint("bootloader info OK")

  # Test if framebuffer request worked
  if framebufferRequest.response == nil:
    debugPrint("ERROR: framebuffer request failed")
    while true:
      asm "cli\nhlt"
  debugPrint("framebuffer request OK")

  # Both requests worked - proceed normally
  when USE_TARGET_A:
    targetA()
  else:
    targetB()
