const FS_MAX_DIRS    = 8
const FS_MAX_ENTRIES = 16
const FS_NAME_LEN    = 32
const FS_PATH_LEN    = 32

type
  FSObjectType = enum
    FSObjectFile,
    FSObjectDir

  FSObject = object
    name:  array[FS_NAME_LEN, char]
    ftype: FSObjectType
    size:  int

  FSDirSlot = object
    path:    array[FS_PATH_LEN, char]
    entries: array[FS_MAX_ENTRIES, FSObject]
    count:   int
    used:    bool

var FSDirectoryTable: array[FS_MAX_DIRS, FSDirSlot]

proc strToName(dst: var array[FS_NAME_LEN, char], src: cstring) =
  var i = 0
  while src[i] != '\0' and i < FS_NAME_LEN - 1:
    dst[i] = src[i]; i += 1
  dst[i] = '\0'

proc strToPath(dst: var array[FS_PATH_LEN, char], src: cstring) =
  var i = 0
  while src[i] != '\0' and i < FS_PATH_LEN - 1:
    dst[i] = src[i]; i += 1
  dst[i] = '\0'

proc nameEq(a: array[FS_PATH_LEN, char], b: cstring): bool =
  var i = 0
  while b[i] != '\0':
    if a[i] != b[i]: return false
    i += 1
  return a[i] == '\0'

proc nameToCStr(name: array[FS_NAME_LEN, char]): cstring =
  cast[cstring](unsafeAddr name[0])

proc FSList(path: cstring): ptr FSDirSlot =
  ## Return pointer to the dir slot for path, or nil if not found.
  for i in 0 ..< FS_MAX_DIRS:
    if FSDirectoryTable[i].used and nameEq(FSDirectoryTable[i].path, path):
      return addr FSDirectoryTable[i]
  return nil

proc FSInit() =
  for i in 0..<FS_MAX_DIRS:
    FSDirectoryTable[i].used  = false
    FSDirectoryTable[i].count = 0
  # Populate "/"
  strToPath(FSDirectoryTable[0].path, "/")
  FSDirectoryTable[0].used  = true
  FSDirectoryTable[0].count = 2
  strToName(FSDirectoryTable[0].entries[0].name, "docs")
  FSDirectoryTable[0].entries[0].ftype = FSObjectDir
  FSDirectoryTable[0].entries[0].size  = 0
  strToName(FSDirectoryTable[0].entries[1].name, "readme.txt")
  FSDirectoryTable[0].entries[1].ftype = FSObjectFile
  FSDirectoryTable[0].entries[1].size  = 1234

# ---- size formatting helpers ----

type
  FSSizeUnit = enum
    fsuB, fsuKB, fsuMB, fsuGB

proc intToStr(v: int, buf: var array[32, char], start: int): int =
  var n = v
  if n == 0:
    buf[start] = '0'
    return start + 1

  var tmp {.noinit.}: array[16, char]
  var len = 0
  while n > 0 and len < tmp.len:
    tmp[len] = char(ord('0') + (n mod 10))
    n = n div 10
    len += 1

  var pos = start
  for i in countdown(len - 1, 0):
    buf[pos] = tmp[i]
    pos += 1
  return pos

proc appendCStr(buf: var array[32, char], pos: int, s: cstring): int =
  var i = 0
  var p = pos
  while s[i] != '\0' and p < buf.len - 1:
    buf[p] = s[i]
    p += 1
    i += 1
  buf[p] = '\0'
  return p

proc formatSize(size: int, buf: var array[32, char]) =
  ## Format size as B/KB/MB/GB into buf, bumping >1000 to next unit (except GB).
  const KB = 1000
  const MB = 1000 * KB
  const GB = 1000 * MB

  var n: int
  var unit: FSSizeUnit

  if size >= GB:
    n = size div GB
    unit = fsuGB
  elif size >= MB:
    n = size div MB
    unit = fsuMB
  elif size >= KB:
    n = size div KB
    unit = fsuKB
  else:
    n = size
    unit = fsuB

  if unit == fsuB and n >= 1000:
    n = n div KB
    unit = fsuKB
  elif unit == fsuKB and n >= 1000:
    n = n div KB
    unit = fsuMB
  elif unit == fsuMB and n >= 1000:
    n = n div KB
    unit = fsuGB

  let p = intToStr(n, buf, 0)

  let unitStr: cstring =
    case unit
    of fsuB:  " B"
    of fsuKB: " KB"
    of fsuMB: " MB"
    of fsuGB: " GB"

  discard appendCStr(buf, p, unitStr)

const FS_SIZE_COL = 20 + 20 * FONT_WIDTH

proc cmdLs(path: cstring) =
  ## List directory via DiskFS.
  let list = DiskFSListRoot()
  var sizeBuf {.noinit.}: array[32, char]
  if list.count == 0:
    writeString(20, curY, "(disk not ready)", 0xFF888888'u32, BG)
    curY += FONT_HEIGHT
    return
  var idx = 0
  while idx < list.count:
    let ep = unsafeAddr list.entries[idx]
    writeString(20, curY, cast[cstring](unsafeAddr ep.Name[0]), FG_INPUT, BG)
    formatSize(int(ep.Size), sizeBuf)
    let color =
      (if ep.FSType == FS_FILE: 0xFF88FF88'u32 else: 0xFF88CCFF'u32)
    writeString(FS_SIZE_COL, curY,
                cast[cstring](addr sizeBuf[0]),
                color, BG)
    curY += FONT_HEIGHT
    idx += 1

