# Keyboard Handler
# ============================================================================

proc keyboardHandler() {.exportc, noconv.} =
  let sc = inb(0x60)
  # Ignore key releases (bit 7 set)
  if (sc and 0x80) != 0:
    picEOI(); return

  let ch = scancodeMap[sc]
  case ch
  of '\0':
    discard
  of '\b':
    if curX > INPUT_X0:
      curX -= FONT_WIDTH
      putChar(curX, curY, ' ', BG, BG)
      if inputLen > 0:
        inputLen -= 1
  of '\n':
    # Null-terminate and dispatch command
    inputBuf[inputLen] = '\0'
    curY += FONT_HEIGHT
    curX = 20
    # Command dispatch
    if inputLen == 2 and inputBuf[0] == 'l' and inputBuf[1] == 's':
      cmdLs("/")
    # Reset buffer and draw new prompt
    inputLen = 0
    writeString(curX, curY, PROMPT, 0xFFFFFFFF'u32, BG)
    curX = INPUT_X0
  else:
    if inputLen < 127:
      inputBuf[inputLen] = ch
      inputLen += 1
    putChar(curX, curY, ch, FG_INPUT, BG)
    curX += FONT_WIDTH

  picEOI()

proc initInterrupts() =
  picRemap()
  # INT 33 = IRQ1 (keyboard). 0x28 = Limine kernel CS. 0x8E = present interrupt gate
  setIdtGate(33, cast[uint64](keyboardIsrStub), 0x28, 0x8E)
  loadIdt()

