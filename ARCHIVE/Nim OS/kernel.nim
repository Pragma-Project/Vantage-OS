proc setIdtGate(vec: int, handler: uint64, sel: uint16, attr: uint8) =
  idt[vec].offsetLow  = uint16(handler and 0xFFFF)
  idt[vec].selector   = sel
  idt[vec].ist        = 0
  idt[vec].typeAttr   = attr
  idt[vec].offsetMid  = uint16((handler shr 16) and 0xFFFF)
  idt[vec].offsetHigh = uint32(handler shr 32)
  idt[vec].zero       = 0

proc loadIdt() =
  idtPtr.limit = uint16(sizeof(idt) - 1)
  idtPtr.base  = cast[uint64](addr idt[0])
  asm """
    lidt %0
    :
    : "m"(`idtPtr`)
  """

