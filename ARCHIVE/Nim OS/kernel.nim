proc serialWrite(c: char) =
  # COM1 port 0x3F8
  asm """
    movb %0, %%al
    movw $0x3F8, %%dx
    outb %%al, %%dx
    :
    : "r"(`c`)
    : "al", "dx"
  """

proc debugPrint(s: cstring) =
  var i = 0
  while s[i] != '\0':
    serialWrite(s[i])
    i += 1
  serialWrite('\n')

proc debugHex(val: uint64) =
  const hexChars = "0123456789ABCDEF"
  for i in countdown(15, 0):
    let nibble = (val shr (i * 4)) and 0xF
    serialWrite(hexChars[nibble.int])
  serialWrite('\n')

