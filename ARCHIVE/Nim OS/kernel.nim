# Target B: Logo display
# ============================================================================

# External symbols from linker (logo.bin embedded)
var binary_logo_bin_start {.importc: "_binary_logo_bin_start".}: uint8
var binary_logo_bin_end {.importc: "_binary_logo_bin_end".}: uint8

const LOGO_WIDTH  = 1280
const LOGO_HEIGHT = 800

proc blitBackground() =
  # Render logo.bin as fullscreen background, BG color fills any uncovered area
  let logoData = cast[ptr UncheckedArray[uint32]](addr binary_logo_bin_start)
  let w = min(fb.width.int, LOGO_WIDTH)
  let h = min(fb.height.int, LOGO_HEIGHT)
  # Blit logo pixels
  for y in 0..<h:
    for x in 0..<w:
      putPixel(x, y, logoData[y * LOGO_WIDTH + x])
  # Fill any rows below the logo
  for y in h..<fb.height.int:
    for x in 0..<fb.width.int:
      putPixel(x, y, BG)
  # Fill any columns to the right of the logo
  for y in 0..<h:
    for x in w..<fb.width.int:
      putPixel(x, y, BG)

proc targetB() =
  if not initFramebuffer():
    panic("No framebuffer")

  blitBackground()

  # Halt loop with interrupts enabled
  while true:
    asm """
      sti
      hlt
    """

