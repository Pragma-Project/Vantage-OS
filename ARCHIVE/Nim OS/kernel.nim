proc putChar(x, y: int, ch: char, fg, bg: uint32) =
  if ch.int < 32 or ch.int > 126:
    return

  let idx = (ch.int - 32) * 16
  for row in 0..<FONT_HEIGHT:
    let bits = font8x16[idx + row]
    for col in 0..<FONT_WIDTH:
      let bit = (bits shr (7 - col)) and 1
      let color = if bit == 1: fg else: bg
      putPixel(x + col, y + row, color)

proc writeString(x, y: int, s: cstring, fg, bg: uint32) =
  var curX = x
  var i = 0
  while s[i] != '\0':
    putChar(curX, y, s[i], fg, bg)
    curX += FONT_WIDTH
    i += 1

