# ============================================================================
# I/O Port Access
# ============================================================================

proc outb(port: uint16, val: uint8) {.inline.} =
  asm """
    outb %1, %0
    :
    : "Nd"(`port`), "a"(`val`)
  """

proc inb(port: uint16): uint8 {.inline.} =
  var r: uint8
  asm """
    inb %1, %0
    : "=a"(`r`)
    : "Nd"(`port`)
  """
  return r

proc ioWait() {.inline.} =
  outb(0x80, 0)

